public class CalculusSystemNumberException extends Exception {
    public CalculusSystemNumberException() {
    }

    public CalculusSystemNumberException(String message) {
        super(message);
    }

    public CalculusSystemNumberException(String message, Throwable cause) {
        super(message, cause);
    }

    public CalculusSystemNumberException(Throwable cause) {
        super(cause);
    }

    public CalculusSystemNumberException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
